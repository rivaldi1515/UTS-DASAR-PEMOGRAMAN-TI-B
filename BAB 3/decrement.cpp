#include <iostream>
using namespace std;

int main()
{
    int k;
    float l;

    k = 100;
    l = 10.5;

    cout << "Nilai k awal : " << k << endl;
    cout << "nilai --k    : " << --k << endl;
    cout << "Nilai k akhir: " << k << endl << endl;

    cout << "Nilai l awal : " << l << endl;
    cout << "nilai --l    : " << --l << endl;
    cout << "Nilai l akhir: " << l << endl << endl;

    return 0;

}

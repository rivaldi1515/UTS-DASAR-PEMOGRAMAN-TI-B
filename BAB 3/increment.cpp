#include <iostream>
using namespace std;

int main ()
{
    int i, j;
    i = 5;
    cout << "Nilai i awal : " << i << endl;
    cout << " Nilai C++   : " << ++i << endl;
    cout << "Nilai i akhir: " << i << endl << endl;

    j = 10;
    cout << "nilai j awal : " << j << endl;
    cout << "Nilai j++    : " << j++ <<endl;
    cout << "Nilai j akhir: " << j << endl << endl;

    return 0;

}
